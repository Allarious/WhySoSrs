    numberOfIteration = str2num(get(S.ed3, 'string'));
    c = bisectionWithNumberOfIteration(f, interval, numberOfIteration);
    
    f = figure('units','pixels',...
       'menubar','none',...
       'position',[750 510 200 100]); % Create a new GUI.
E = uicontrol('style','text',...
              'units','pixels',...
              'position',[10 20 180 60],...
              'string',c);
          
        
    end
    function [] = pb_call2(varargin)
    % Callback for the pushbutton.
    interval = str2num(get(S.ed1, 'string'));
    f = get(S.ed2, 'string');
    numberOfIteration = str2num(get(S.ed3, 'string'));
    c = bisectionWithPrecision(f, interval, numberOfIteration);
    
    f = figure('units','pixels',...
