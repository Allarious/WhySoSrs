                'string','');
S.ed2 = uicontrol('style','edit',...
                 'units','pix',...
                'position',[250 250 180 40],...
                'string','');
S.ed3 = uicontrol('style','edit',...
                 'units','pix',...
                'position',[250 200 180 40],...
                'string','');
S.tx1 = uicontrol('style','text',...
                 'units','pix',...
                'position',[60 290 180 40],...
                'string','Interval');
S.tx2 = uicontrol('style','text',...
                 'units','pix',...
                'position',[60 240 180 40],...
